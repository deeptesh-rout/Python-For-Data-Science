# Creating Lists of fruits with strings
fruits = ["banana", "apples", "peers", "pineapples", "oranges"]
# index = [0,1,2,3,4]

# Accessing elements in Lists
print(fruits[0])
print(fruits[2])
print(fruits[4])

# Accessing the last element in the list using the len function
print(fruits[len(fruits)-1])
print(fruits[-1])

# Modifying an element in a List
fruits[2] = "grapes"
print(fruits)

# Creating Matrix
matrix = [[4,5,6], [6,7,9], [10,22,35]]
print(matrix)

# Accessing Elements in Matrix
print(matrix[0][0])
print(matrix[1][1])
print(matrix[2][1])

# Modifying Elements in Matrix
matrix[1][2] = -999
print(matrix)


